"""
Статическое определение квиза — 3 вопроса (по одному на аспект).
Квиз в текущем скоупе не редактируется из админки, поэтому живёт в коде.

У каждого варианта ответа явно задан уровень (1/2/3), потому что вариантов
больше трёх. С фронта приходит ИНДЕКС выбранного варианта (1-based), а не
уровень: два разных варианта могут иметь одинаковый уровень, поэтому индекс
нужен и для уникального выбора в интерфейсе, и для аудита ответов.
"""

from app.scoring import Aspect

# По одному вопросу на аспект. options: [{"text", "level"}], индекс = позиция (1-based).
QUIZ = [
    {
        "code": "M",
        "aspect": Aspect.MARKETING,
        "text": "Как к вам приходят новые клиенты?",
        "options": [
            {"text": "Только сарафан/партнерка", "level": 1},
            {"text": "Эпизодически срабатывают соц. сети/авито/сайт/выступления/ТГ-канал и т.д.", "level": 1},
            {"text": "Системный поток из управляемых инструментов (от 8 заявок в неделю)", "level": 2},
            {"text": "Система из нескольких каналов + сарафан + партнерка, есть руководитель по маркетингу", "level": 3},
        ],
    },
    {
        "code": "S",
        "aspect": Aspect.SALES,
        "text": "Как устроены ваши продажи?",
        "options": [
            {"text": "Лично с первого звонка. Консультирую, а не продаю.", "level": 1},
            {"text": "Лично с первого звонка. Продаю, но и консультирую тоже.", "level": 1},
            {"text": "Есть секретарь/лид-менеджер, который отсекает неликвид, дальше лично.", "level": 2},
            {"text": "Есть лид-менеджер и другой продающий основные услуги сотрудник вместе со мной.", "level": 2},
            {"text": "Есть отдел продаж, я не вовлечен (-а) в продажи.", "level": 3},
            {"text": "Есть РОП, менеджеры, планы, вы смотрите только на цифры.", "level": 3},
        ],
    },
    {
        "code": "Mg",
        "aspect": Aspect.MANAGEMENT,
        "text": "Как устроено исполнение обязательств?",
        "options": [
            {"text": "Все сам (-а), максимум есть 1 помощник", "level": 1},
            {"text": "Есть младшие юристы, которым нужно нарезать задачи и все контролировать. Вы самый умный (-ая) в команде;", "level": 1},
            {"text": "Есть старший юрист, который курирует младших, но вы по прежнему лучший эксперт в команде;", "level": 2},
            {"text": "Есть руководители по направлениям или отделам, вы не вовлечены в стратегию ведения дел.", "level": 3},
        ],
    },
]

QUIZ_CODES = [q["code"] for q in QUIZ]
_BY_CODE = {q["code"]: q for q in QUIZ}


def answers_to_levels(answers: dict[str, int]) -> dict[Aspect, int]:
    """
    Преобразует ответы {code: option_index(1-based)} в {aspect: level}
    для передачи в движок скоринга. Проверяет полноту и корректность.
    """
    if set(answers) != set(QUIZ_CODES):
        raise ValueError("Нужны ответы ровно на все 3 вопроса")

    by_aspect: dict[Aspect, int] = {}
    for code in QUIZ_CODES:  # стабильный порядок
        index = answers[code]
        options = _BY_CODE[code]["options"]
        if not isinstance(index, int) or not (1 <= index <= len(options)):
            raise ValueError(f"Некорректный вариант ответа на вопрос {code}")
        by_aspect[_BY_CODE[code]["aspect"]] = options[index - 1]["level"]
    return by_aspect
