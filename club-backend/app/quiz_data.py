QUIZ_CODES = [q["code"] for q in QUIZ]
_BY_CODE = {q["code"]: q for q in QUIZ}


def answers_to_levels(answers: dict[str, int]) -> dict[Aspect, list[int]]:
    """
    Преобразует ответы {code: option_index(1..3)} в {aspect: [levels]}
    для передачи в движок скоринга. Проверяет полноту и корректность.
    """
    if set(answers) != set(QUIZ_CODES):
        raise ValueError("Нужны ответы ровно на все 9 вопросов")

    by_aspect: dict[Aspect, list[int]] = {
        Aspect.MARKETING: [],
        Aspect.SALES: [],
        Aspect.MANAGEMENT: [],
    }
    for code in QUIZ_CODES:  # стабильный порядок
        value = answers[code]
        if value not in (1, 2, 3):
            raise ValueError(f"Ответ на {code} должен быть 1, 2 или 3")
        by_aspect[_BY_CODE[code]["aspect"]].append(value)
    return by_aspect
