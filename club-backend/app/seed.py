def seed() -> None:
    with Session(engine) as session:
        if session.exec(select(TrajectoryHint)).first() is None:
            for (aspect, level), text in HINTS.items():
                session.add(TrajectoryHint(aspect=aspect, level=level, hint_text=text))

        if session.exec(select(ContentCard)).first() is None:
            for (aspect, level), titles in CARDS.items():
                for pos, title in enumerate(titles, start=1):
                    session.add(ContentCard(
                        aspect=aspect, level=level, position=pos, title=title,
                    ))

        if session.exec(select(User).where(User.email == ADMIN_EMAIL)).first() is None:
            session.add(User(
                email=ADMIN_EMAIL,
                password_hash=hash_password(ADMIN_PASSWORD),
                role="admin",
            ))

        session.commit()
